#!/usr/bin/python3
"""
This script contains utility functions for basic arithmetic operations.
"""


def number_keys(a_dictionary):
    """
    Return the number of keys in a dictionary.

    This function calculates the total number of keys in the provided
    dictionary and returns that count.

    Parameters:
    a_dictionary (dict): The dictionary whose keys are to be counted.

    Returns:
    int: The number of keys in the dictionary.
    """
    return (len(a_dictionary))
